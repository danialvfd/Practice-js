/* متد substring 
برای خارج کردن قسمتی از متن در string
*/
const sampleText1 = "Hello, World!";
const sampleResult1 = sampleText1.substring(0, 5); // خود 5 نیست
console.log(sampleResult1); 

const sampleText2 = "JavaScript is fun!";
const sampleResult2 = sampleText2.substring(11);
console.log(sampleResult2); // بعد از 11 همین کاراکتر در استرینگ را خارج می کند
